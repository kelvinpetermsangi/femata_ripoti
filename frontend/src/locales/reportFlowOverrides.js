const questionBranches = [
  "miner",
  "license",
  "investor",
  "worker",
  "association",
  "vendor",
  "community",
  "general",
];

const assignIndexed = (target, prefix, values, suffix = "") => {
  values.forEach((value, index) => {
    target[`${prefix}${index + 1}${suffix}`] = value;
  });
};

const assignQuestions = (target, questions) => {
  questionBranches.forEach((branch) => {
    questions[branch].forEach((entry, index) => {
      target[`reportQuestion${branch}${index + 1}Label`] = entry.label;
      entry.options.forEach((option, optionIndex) => {
        target[`reportQuestion${branch}${index + 1}Option${optionIndex + 1}`] = option;
      });
    });
  });
};

const buildLocale = (config) => {
  const result = { ...config.common };
  assignIndexed(result, "reportGroup", config.groupTitles, "Title");
  assignIndexed(result, "reportGroup", config.groupBodies, "Body");
  assignIndexed(result, "reportValueRole", config.valueRoles);
  assignIndexed(result, "reportTarget", config.targets);
  assignIndexed(result, "reportTheme", config.themes);
  assignIndexed(result, "reportHandlingLevel", config.handlingLevels);
  assignIndexed(result, "reportSeverity", config.severities);
  assignIndexed(result, "reportAffectedScope", config.affectedScopes);
  assignQuestions(result, config.questions);
  return result;
};

